package JavaBasics;

public class LoopsinJava {

	public static void main(String[] args) {
	
		//1.While loop 
		// I want to print from 1-10 Disadvantage it will give infinite if we dont give incremental or decremental
		
		int i =1 ;//intialization
		
		while(i<=10)//conditional part
			{
			System.out.println(i);
			i=i+1;//incremental or decremental part
		}
		
		System.out.println("**************");
		
		
		//2.For loop everything in the same line initi  condit incre/decre
		for(int k =1;k<=10;k++) {
			System.out.println(k);
			
		}
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
